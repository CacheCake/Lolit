package vo;

public class TeamData {
	private String serverCount;
	private String genderCount;
	private String winCount;
	private String areaCount;
	public String getServerCount() {
		return serverCount;
	}
	public void setServerCount(String serverCount) {
		this.serverCount = serverCount;
	}
	public String getGenderCount() {
		return genderCount;
	}
	public void setGenderCount(String genderCount) {
		this.genderCount = genderCount;
	}
	public String getWinCount() {
		return winCount;
	}
	public void setWinCount(String winCount) {
		this.winCount = winCount;
	}
	public String getAreaCount() {
		return areaCount;
	}
	public void setAreaCount(String areaCount) {
		this.areaCount = areaCount;
	}
	
	
}
